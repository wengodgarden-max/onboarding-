function detectBreakpoint(message, history) {
  const recentHistory = (history || []).slice(-4).map(m => (m.content || m.text || '')).join(' ');
  const fullContext = (message + ' ' + recentHistory).toLowerCase();

  const results = [];

  for (const [key, config] of Object.entries(PATTERNS)) {
    for (const kw of config.keywords) {
      try {
        const re = new RegExp(kw);
        if (re.test(fullContext)) {
          results.push({
            type: key,
            prompt: config.prompt,
            deliverable: config.deliverable,
            deliverableLabel: config.deliverableLabel,
            matchedKeyword: kw,
            score: 1,
          });
          break;
        }
      } catch (_) {
        if (fullContext.includes(kw)) {
          results.push({
            type: key,
            prompt: config.prompt,
            deliverable: config.deliverable,
            deliverableLabel: config.deliverableLabel,
            matchedKeyword: kw,
            score: 0.8,
          });
          break;
        }
      }
    }
  }

  results.sort((a, b) => b.score - a.score);
  return results;
}

function getBreakpointPrompt(type) {
  return PATTERNS[type] ? PATTERNS[type].prompt : null;
}

